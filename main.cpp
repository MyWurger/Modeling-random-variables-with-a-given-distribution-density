// Назначение файла:
// 1) выполнить стартовую инициализацию Qt-приложения;
// 2) настроить поиск Qt-плагинов внутри app bundle на macOS;
// 3) создать и показать главное окно MainWindow;
// 4) передать управление в главный цикл обработки событий Qt.
//
// Дополнительно этот файл отвечает за важную практическую деталь:
// приложение должно запускаться не только из среды разработки, но и как
// собранный .app bundle, где плагины Qt лежат рядом с исполняемым файлом
// во внутренней структуре пакета.
// ============================================================================

// Подключаем объявление главного окна приложения.
#include "MainWindow.h"
// QApplication - базовый объект любого Qt Widgets-приложения.
#include <QApplication>
// QCoreApplication нужен для явной настройки путей поиска библиотек и плагинов Qt.
#include <QCoreApplication>
// QDir используется для работы с каталогами рядом с исполняемым файлом.
#include <QDir>
// QFileInfo нужен для получения абсолютного пути к исполняемому файлу.
#include <QFileInfo>
// QFont нужен для настройки общего шрифта интерфейса.
#include <QFont>
// QStringList удобен для хранения и фильтрации списка путей плагинов.
#include <QStringList>

// ----------------------------------------------------------------------------
// ЛОКАЛЬНЫЕ ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
// ----------------------------------------------------------------------------
// Все сущности в анонимном namespace видны только внутри данного .cpp файла.
// ----------------------------------------------------------------------------
namespace
{
// ----------------------------------------------------------------------------
// ФУНКЦИЯ ConfigureQtPluginPaths
// ----------------------------------------------------------------------------
// Назначение:
// Настроить для Qt корректные пути поиска плагинов внутри app bundle.
//
// Зачем это нужно:
// На macOS после упаковки приложения плагины платформы, SVG icon engine и
// другие модули Qt лежат не в системных каталогах, а внутри структуры:
// ModelingMethodsLab1.app/Contents/PlugIns
//
// Если явно не подсказать Qt, где искать эти плагины, приложение может не
// найти, например, platform plugin "cocoa" и завершиться еще до показа окна.
//
// Принимает:
// - argc : число аргументов командной строки;
// - argv : массив аргументов командной строки.
// ----------------------------------------------------------------------------
void ConfigureQtPluginPaths(int argc, char* argv[])
{
    // Сначала пробуем определить абсолютный путь к исполняемому файлу.
    //
    // argv[0] обычно содержит путь запуска, но дополнительно проверяем
    // корректность argc, argv и самого первого аргумента.
    QString executablePath;
    if (argc > 0 && argv != nullptr && argv[0] != nullptr)
    {
        executablePath = QFileInfo(QString::fromLocal8Bit(argv[0])).absoluteFilePath();
    }

    // applicationDir - каталог, в котором лежит сам исполняемый файл.
    //
    // Если путь к executable определить не удалось, используем текущий рабочий
    // каталог как запасной вариант.
    const QString applicationDir =
        executablePath.isEmpty() ? QDir::currentPath() : QFileInfo(executablePath).absolutePath();

    // Внутри macOS bundle каталог плагинов лежит рядом с бинарником уровнем
    // выше: ../PlugIns
    const QString bundledPluginPath = QDir(applicationDir).absoluteFilePath("../PlugIns");

    // Пока что у нас один основной кандидат на путь плагинов - bundledPluginPath.
    // Но хранить его удобнее в списке, чтобы затем одинаково обработать.
    QStringList pluginPaths = {bundledPluginPath};

    // Здесь оставим только реально существующие каталоги.
    QStringList validPaths;
    for (const QString& path : pluginPaths)
    {
        // Проверяем, что каталог действительно существует.
        if (QDir(path).exists())
        {
            validPaths.append(QDir(path).absolutePath());
        }
    }

    // На всякий случай убираем возможные дубликаты.
    validPaths.removeDuplicates();

    // Если нашлись рабочие каталоги плагинов, передаем их Qt.
    if (!validPaths.isEmpty())
    {
        // QT_PLUGIN_PATH - общий путь, по которому Qt может искать плагины.
        qputenv("QT_PLUGIN_PATH", validPaths.join(':').toUtf8());

        // Для platform plugin дополнительно задаем более узкий каталог
        // platforms, чтобы запуск на macOS был максимально надежным.
        const QString platformPath =
            QDir(validPaths.front()).absoluteFilePath("platforms");
        if (QDir(platformPath).exists())
        {
            qputenv("QT_QPA_PLATFORM_PLUGIN_PATH", platformPath.toUtf8());
        }

        // QCoreApplication::setLibraryPaths(...) сообщает Qt внутренний список
        // каталогов, где разрешено искать плагины уже после старта приложения.
        QCoreApplication::setLibraryPaths(validPaths);
    }
}
} // namespace

// ----------------------------------------------------------------------------
// ФУНКЦИЯ main
// ----------------------------------------------------------------------------
// Назначение:
// Выполнить стартовую инициализацию приложения и запустить графический
// интерфейс лабораторной работы.
//
// Принимает:
// - argc : число аргументов командной строки;
// - argv : массив аргументов командной строки.
//
// Возвращает:
// - код завершения приложения, который возвращает цикл обработки событий Qt.
// ----------------------------------------------------------------------------
int main(int argc, char* argv[])
{
    // До создания QApplication настраиваем пути к Qt-плагинам.
    //
    // Это важно сделать заранее, потому что уже при инициализации QApplication
    // Qt может пытаться загрузить platform plugin.
    ConfigureQtPluginPaths(argc, argv);

    // QApplication создается самым первым среди крупных GUI-объектов.
    // Без него нельзя создавать и показывать Qt-виджеты.
    QApplication app(argc, argv);

    // Задаем читаемое имя приложения.
    app.setApplicationName("Моделирование случайных величин");

    // Имя организации используется Qt в служебных сценариях, например
    // при хранении пользовательских настроек.
    app.setOrganizationName("ModelingMethodsLab1");

    // Подготавливаем общий шрифт интерфейса.
    QFont uiFont("Avenir Next", 11);

    // PreferAntialias просит Qt использовать более качественное сглаживание
    // текста, если это возможно на текущей платформе.
    uiFont.setStyleStrategy(QFont::PreferAntialias);

    // Устанавливаем этот шрифт приложению целиком, чтобы интерфейс выглядел
    // единообразно.
    app.setFont(uiFont);

    // Создаем главное окно приложения.
    MainWindow window;

    // Показываем окно на экране.
    window.show();

    // app.exec() запускает главный цикл обработки событий Qt.
    //
    // После этого приложение начинает:
    // - реагировать на действия пользователя;
    // - обновлять интерфейс;
    // - обрабатывать события мыши, клавиатуры, таймеров и графики.
    return app.exec();
}
