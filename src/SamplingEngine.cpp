// ----------------------------------------------------------------------------
// ФУНКЦИЯ ComputeStatistics - ВЫЧИСЛЕНИЕ ВЫБОРОЧНЫХ ХАРАКТЕРИСТИК
// ----------------------------------------------------------------------------
// По уже сгенерированной выборке вычисляем:
// - минимум и максимум;
// - выборочное среднее;
// - выборочную дисперсию;
// - среднеквадратическое отклонение;
// - медиану.
//
// Важно, что медиана требует отсортированной выборки,
// поэтому функция принимает сразу две версии данных:
// исходную и отсортированную.
//
// Принимает:
// - samples: исходную выборку в том порядке, в котором она была сгенерирована;
// - sortedSamples: ту же выборку, но уже отсортированную по возрастанию.
//
// Возвращает:
// - структуру SamplingStatistics со всеми основными выборочными характеристиками.
// ----------------------------------------------------------------------------
SamplingStatistics ComputeStatistics(const std::vector<double>& samples,
                                    const std::vector<double>& sortedSamples)
{
    // Защищаемся от некорректного вызова функции:
    // обе выборки должны существовать, быть непустыми и иметь одинаковый размер.
    if (samples.empty() || sortedSamples.empty() || samples.size() != sortedSamples.size())
    {
        throw std::invalid_argument("Выборка и её сортированная версия должны быть корректны");
    }

    // Создаем структуру, в которую будем поэтапно складывать все статистики.
    SamplingStatistics stats;

    // Минимум и максимум нужны как сами по себе,
    // так и для общей сводки по выборке.
    stats.minimum = *std::min_element(samples.begin(), samples.end());
    stats.maximum = *std::max_element(samples.begin(), samples.end());

    // Вычисляем сумму всех элементов выборки.
    const double sum = std::accumulate(samples.begin(), samples.end(), 0.0);
    // Выборочное среднее = сумма / число элементов.
    stats.mean = sum / static_cast<double>(samples.size());

    // Накапливаем сумму квадратов отклонений от среднего,
    // чтобы затем получить выборочную дисперсию.
    double varianceAccumulator = 0.0;

    // проходимся по всем элементам выборки
    for (double value : samples)
    {
        // Отклонение текущего элемента выборки от среднего значения.
        const double delta = value - stats.mean;
        // Прибавляем квадрат отклонения.
        // Квадрат нужен затем, чтобы:
        // - убрать знак отклонения;
        // - сильнее учитывать большие отклонения.
        varianceAccumulator += delta * delta;
    }

    // Делим накопленную сумму на размер выборки
    // и получаем выборочную дисперсию.
    stats.variance = varianceAccumulator / static_cast<double>(samples.size());
    // Среднеквадратическое отклонение - это корень из дисперсии.
    stats.standardDeviation = std::sqrt(stats.variance);

    // Находим индекс середины отсортированной выборки.
    // Именно относительно этой позиции определяется медиана.
    const std::size_t middle = sortedSamples.size() / 2;

    // Если число элементов четное,
    // то единственного центрального элемента нет.
    // Поэтому медиана равна среднему двух центральных значений.
    if (sortedSamples.size() % 2 == 0)
    {
        stats.median = 0.5 * (sortedSamples[middle - 1] + sortedSamples[middle]);
    }
    else
    {
        // Если число элементов нечетное,
        // медианой является ровно центральный элемент.
        stats.median = sortedSamples[middle];
    }

    // Возвращаем структуру результата.
    return stats;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ GenerateInverseFunctionSamples - МЕТОД ОБРАТНОЙ ФУНКЦИИ
// ----------------------------------------------------------------------------
// Теория лабораторной говорит следующее:
// если F(x) - функция распределения, а k - равномерно распределенная случайная
// величина на [0, 1], то значение x можно находить из уравнения
// F(x) = k.
//
// Алгоритм генерации СВ с использованием метода обратной функции
// по теории ЛР формулируется так:
// 1) с помощью датчика псевдослучайных чисел сгенерировать значение k
//    случайной величины, распределенной по равномерному закону
//    в интервале [0; 1];
// 2) с использованием численного метода вычисления определенного интеграла
//    рассчитать значение случайной величины следующим образом:
//    2.1) принять значение y = -A, S = 0, задать величину шага
//         интегрирования Δy;
//    2.2) вычислять значение S и y по формулам
//         S = S + p(y) * Δy,
//         y = y + Δy,
//         до тех пор, пока значение S не превысит k,
//         то есть пока не выполнится условие S >= k;
//    2.3) в качестве значения случайной величины принять текущее значение y.
//
// Для нашего варианта алгоритм немного упрощается:
// - плотность отлична от нуля только на отрезке [0, 1];
// - поэтому роль стартовой точки фактически играет левая граница носителя,
//   то есть y = 0;
// - отдельный подбор A не нужен, потому что вне [0, 1] плотность равна нулю.
//
// В коде ниже реализована именно эта численная идея,
// но в оптимизированном виде:
// - сначала один раз строится таблица накопленных вероятностей S(y);
// - затем для каждого нового k подходящее значение y ищется быстрее,
//   без повторного прохода с самого начала.
//
// Принимает:
// - sampleSize: сколько значений нужно сгенерировать;
// - integrationStep: шаг Δy для численного накопления интеграла.
//
// Возвращает:
// - вектор сгенерированных значений случайной величины.
// ----------------------------------------------------------------------------
std::vector<double> GenerateInverseFunctionSamples(std::size_t sampleSize, double integrationStep)
{
    // В методе обратной функции шаг интегрирования обязательно должен быть
    // положительным, иначе невозможно построить численное накопление интеграла.
    // Если шаг был бы нулевым или отрицательным, цикл по y либо вообще
    // не продвигался бы вперед, либо работал бы неверно.
    if (!(integrationStep > 0.0))
    {
        throw std::invalid_argument("Шаг интегрирования должен быть больше нуля");
    }

    // Подготавливаем контейнер для итоговой выборки.
    // Именно сюда будут записываться все сгенерированные значения случайной
    // величины после применения метода обратной функции.
    std::vector<double> samples;
    // reserve заранее выделяет память под нужное число элементов.
    // Это уменьшает число перераспределений памяти при заполнении вектора.
    samples.reserve(sampleSize);

    // cumulativeProbabilities хранит значения накопленной вероятности,
    // а gridValues - соответствующие точки сетки по x.
    //
    // Иначе говоря:
    // - cumulativeProbabilities[i] - это численная оценка интеграла
    //   от левой границы до точки gridValues[i];
    // - gridValues[i] - это то значение y, которому соответствует
    //   накопленная вероятность cumulativeProbabilities[i].
    //
    // Вместе эти два массива задают табличную аппроксимацию функции
    // распределения F(y), построенную численным методом.
    std::vector<double> cumulativeProbabilities;
    std::vector<double> gridValues;

    // Для нашего варианта носитель распределения ограничен отрезком [0, 1].
    // Это значит, что:
    // - левее 0 плотность равна нулю;
    // - правее 1 плотность тоже равна нулю;
    // - поэтому интеграл имеет смысл накапливать именно на этом отрезке.
    const double supportMin = TVariantDistribution::SupportMin();
    const double supportMax = TVariantDistribution::SupportMax();

    // Оцениваем, сколько шагов понадобится для прохода по всему носителю.
    // ceil нужен, чтобы гарантированно покрыть весь отрезок.
    //
    // Пример:
    // если длина отрезка равна 1, а шаг Δy = 0.03,
    // то 1 / 0.03 = 33.33..., значит потребуется 34 шага,
    // чтобы не оборвать проход раньше правой границы.
    const std::size_t stepCount = std::max<std::size_t>(
        1,
        static_cast<std::size_t>(std::ceil((supportMax - supportMin) / integrationStep)));
    // Заранее резервируем память и для таблицы вероятностей, и для сетки значений.
    cumulativeProbabilities.reserve(stepCount);
    gridValues.reserve(stepCount);

    // Шаг 2.1 из теории:
    // принимаем стартовое значение y на левой границе носителя.
    // В общем случае теория говорит про y = -A,
    // но для нашего распределения поддержка начинается сразу с 0.
    //
    // A в теории - это "достаточно большое число",
    // позволяющее заменить бесконечную левую границу -∞
    // на обычную конечную точку -A.
    // Ее выбирают так, чтобы левее этой точки плотность была уже
    // настолько мала, что ею можно пренебречь.
    //
    // В нашем варианте такое упрощение не требуется,
    // потому что левее 0 плотность равна нулю точно, а не приблизительно.
    double y = supportMin;
    // Шаг 2.1 из теории:
    // стартовое значение накопленной вероятности S равно нулю.
    // До начала интегрирования вероятность "накопить что-то левее supportMin"
    // отсутствует, поэтому стартуем с нуля.
    double accumulatedProbability = 0.0;

    // Шаг 2.2 из теории:
    // идем по сетке слева направо и повторяем схему
    // S = S + p(y) * Δy, y = y + Δy.
    //
    // Фактически здесь мы строим численную таблицу значений функции
    // распределения F(y), чтобы потом не интегрировать заново
    // для каждого нового случайного числа k.
    while (y < supportMax)
    {
        // Метод левых прямоугольников:
        // прибавляем площадь маленького прямоугольника p(y) * Δy.
        //
        // Это и есть численная аппроксимация определенного интеграла:
        // вместо точного значения площади под кривой берем сумму
        // площадей узких прямоугольников.
        accumulatedProbability += TVariantDistribution::Density(y) * integrationStep;

        // Переходим к следующей точке сетки,
        // но не выходим правее supportMax.
        //
        // std::min нужен, чтобы последний шаг аккуратно завершился ровно
        // на правой границе отрезка, а не ушел чуть дальше из-за шага Δy.
        y = std::min(y + integrationStep, supportMax);

        // Сохраняем пару:
        // "какая накопленная вероятность достигнута к моменту y".
        //
        // После этого:
        // - cumulativeProbabilities.back() хранит текущее значение S;
        // - gridValues.back() хранит соответствующее значение y.
        cumulativeProbabilities.push_back(accumulatedProbability);
        gridValues.push_back(y);
    }

    // Создаем генератор псевдослучайных чисел.
    // std::mt19937_64 - это конкретный алгоритм генерации псевдослучайных
    // чисел большой длины периода.
    // std::random_device{}() используется как источник начального зерна,
    // чтобы последовательность не была одинаковой при каждом запуске.
    std::mt19937_64 generator(std::random_device{}());
    // Генерируем равномерные числа на [0, 1].
    // Это как раз соответствует теоретическому требованию:
    // получить случайную величину k, равномерно распределенную на [0, 1].
    std::uniform_real_distribution<double> unitDistribution(0.0, 1.0);

    // Для каждого элемента будущей выборки повторяем шаг 1 теории:
    // генерируем новое значение k на интервале [0, 1].
    //
    // Здесь уже не перестраивается таблица интеграла:
    // она была подготовлена заранее один раз.
    for (std::size_t index = 0; index < sampleSize; ++index)
    {
        // Шаг 1 из теории:
        // генерируем k ~ U[0, 1].
        const double k = unitDistribution(generator);

        // Шаг 2.2 из теории в ускоренном виде:
        // ищем первое значение накопленной вероятности S,
        // для которого уже выполнено условие S >= k.
        //
        // std::lower_bound работает на отсортированном диапазоне
        // и возвращает итератор на первый элемент, который не меньше k.
        //
        // Так как cumulativeProbabilities заполняется по мере роста интеграла,
        // этот массив монотонно неубывает, а значит бинарный поиск корректен.
        const auto it = std::lower_bound(cumulativeProbabilities.cbegin(),
                                         cumulativeProbabilities.cend(),
                                         k);

        // Если из-за погрешностей интегрирования k оказалось чуть больше
        // последнего накопленного значения, берем правую границу носителя.
        //
        // Это защитный случай:
        // из-за численной ошибки сумма площадей может оказаться немного меньше 1.
        if (it == cumulativeProbabilities.cend())
        {
            samples.push_back(supportMax);
            continue;
        }

        // Шаг 2.3 из теории:
        // в качестве значения случайной величины берем текущее y,
        // в котором накопленная вероятность впервые достигла k.
        //
        // std::distance переводит найденный итератор в числовой индекс.
        // Этот индекс одинаков для двух параллельных таблиц:
        // - cumulativeProbabilities;
        // - gridValues.
        const std::size_t lookupIndex =
            static_cast<std::size_t>(std::distance(cumulativeProbabilities.cbegin(), it));
        // Значение gridValues[lookupIndex] и есть искомая реализация
        // случайной величины, соответствующая данному k.
        samples.push_back(gridValues[lookupIndex]);
    }

    // Возвращаем полностью сформированную выборку,
    // сгенерированную методом обратной функции.
    return samples;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ GenerateRejectionSamples - МЕТОД ИСКЛЮЧЕНИЯ
// ----------------------------------------------------------------------------
// По теории ЛР метод исключения работает так:
// 1) находится максимум pmax = M функции плотности;
// 2) выбираются границы a и b, вне которых плотность пренебрежимо мала;
// 3) генерируется значение y, равномерно распределенное на [a, b];
// 4) генерируется значение z, равномерно распределенное на [0, 1];
// 5) вычисляется значение плотности p(y);
// 6) проверяется условие z < (1 / M) * p(y);
// 7) если условие выполнено, y включается в выборку,
//    иначе точка отбрасывается.
//
// Для нашего варианта:
// - носитель распределения: [0, 1];
// - максимум плотности M = 2;
// - плотность f(y) = 2y.
//
// Тогда условие принятия
// z < (1 / M) * f(y)
// упрощается до
// z < y.
//
// Принимает:
// - sampleSize: сколько элементов нужно получить в итоговой выборке.
//
// Возвращает:
// - pair, где:
//   1) первый элемент - сама сгенерированная выборка;
//   2) второй элемент - доля принятия точек в методе исключения.
// ----------------------------------------------------------------------------
std::pair<std::vector<double>, double> GenerateRejectionSamples(std::size_t sampleSize)
{
    // Шаг 1 из теории:
    // максимум плотности уже известен аналитически и равен M = 2.
    const double maxDensity = TVariantDistribution::MaxDensity();

    // Шаг 1 из теории:
    // границы области по оси y для нашего варианта равны a = 0 и b = 1,
    // потому что именно на этом отрезке плотность отлична от нуля.
    const double supportMin = TVariantDistribution::SupportMin();
    const double supportMax = TVariantDistribution::SupportMax();

    // Контейнер для принятой выборки.
    // Сюда будут попадать только те значения y,
    // которые прошли проверку условия принятия.
    std::vector<double> samples;
    // Заранее резервируем память под всю будущую выборку,
    // чтобы уменьшить число перераспределений памяти.
    samples.reserve(sampleSize);

    // Создаем генератор псевдослучайных чисел.
    // Он играет роль "датчика псевдослучайных чисел" из теории.
    std::mt19937_64 generator(std::random_device{}());
    // Так как в нашем варианте:
    // - y генерируется на [a, b] = [0, 1];
    // - z генерируется на [0, 1];
    // достаточно одного равномерного распределения на [0, 1].
    // Каждое обращение unitDistribution(generator) дает новое число
    // из равномерного закона на [0, 1].
    std::uniform_real_distribution<double> unitDistribution(0.0, 1.0);

    // attempts хранит число всех сгенерированных кандидатов.
    // Это нужно, чтобы потом вычислить долю принятия.
    //
    // Иначе говоря:
    // - samples.size() - сколько точек мы приняли;
    // - attempts - сколько точек всего попробовали.
    std::size_t attempts = 0;

    // Продолжаем генерацию, пока не наберем нужный размер выборки.
    //
    // Это соответствует общей логике метода исключения:
    // часть точек попадает в выборку, а часть отбрасывается,
    // поэтому число попыток всегда не меньше числа принятых значений.
    while (samples.size() < sampleSize)
    {
        // Фиксируем, что сделали еще одну попытку сгенерировать точку-кандидат.
        ++attempts;

        // Шаг 2 из теории:
        // генерируем значение y, равномерно распределенное на [a, b].
        //
        // Так как a = 0 и b = 1, дополнительное масштабирование не нужно:
        // число из [0, 1] уже является числом из [a, b].
        const double y = supportMin + (supportMax - supportMin) * unitDistribution(generator);

        // Шаг 2 из теории:
        // генерируем значение z, равномерно распределенное на [0, 1].
        const double z = unitDistribution(generator);

        // Шаг 3 из теории:
        // вычисляем значение плотности в сгенерированной точке y.
        const double density = TVariantDistribution::Density(y);

        // Шаг 4 из теории:
        // проверяем условие z < (1 / M) * p(y).
        //
        // Почему так:
        // 1) плотность нашего варианта равна f(y) = 2y на [0, 1];
        // 2) максимум плотности M = 2;
        // 3) теоретическое условие принятия имеет вид
        //    z < (1 / M) * f(y);
        // 4) подставляем M = 2 и f(y) = 2y:
        //    z < (1 / 2) * 2y = y.
        //
        // Значит, условие можно понимать в двух эквивалентных формах:
        // - как общее z < (1 / M) * p(y);
        // - как упрощенное для нашего варианта z < y.
        if (z < density / maxDensity)
        {
            // Шаг 5 из теории:
            // если условие выполнено, включаем y в выборку.
            // Именно абсцисса y становится новым значением
            // моделируемой случайной величины.
            samples.push_back(y);
        }
        // Если условие не выполнено, точка просто отбрасывается,
        // и цикл переходит к следующей попытке.
    }

    // Доля принятия = число принятых элементов / число всех попыток.
    // Эта величина полезна как характеристика эффективности метода:
    // чем она выше, тем меньше "пустых" сгенерированных точек.
    const double acceptanceRate =
        static_cast<double>(samples.size()) / static_cast<double>(attempts);

    // Возвращаем:
    // 1) саму сформированную выборку;
    // 2) долю принятия, чтобы можно было показать ее в интерфейсе.
    return {samples, acceptanceRate};
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ BuildHistogram - ПОСТРОЕНИЕ ГИСТОГРАММЫ
// ----------------------------------------------------------------------------
// Теория ЛР требует построить гистограмму распределения по сформированной
// выборке. Здесь:
// 1) весь носитель [0, 1] разбивается на bins интервалов;
// 2) для каждого интервала считается число попавших значений;
// 3) частота переводится в оценку плотности.
//
// Принимает:
// - samples: выборку случайных значений;
// - bins: число интервалов, на которые делится носитель распределения.
//
// Возвращает:
// - массив интервалов HistogramBin с границами, количеством попаданий
//   и оценкой плотности в каждом интервале.
// ----------------------------------------------------------------------------
std::vector<HistogramBin> BuildHistogram(const std::vector<double>& samples, std::size_t bins)
{
    // Число интервалов должно быть положительным.
    // Гистограмму нельзя построить без разбиения диапазона хотя бы на один интервал.
    if (bins == 0)
    {
        throw std::invalid_argument("Число интервалов гистограммы должно быть больше нуля");
    }

    // Подготавливаем массив интервалов гистограммы.
    // Каждый элемент этого массива будет описывать один столбец гистограммы:
    // - левую границу интервала;
    // - правую границу интервала;
    // - сколько значений в него попало;
    // - какую оценку плотности этот интервал задает.
    std::vector<HistogramBin> histogram;
    // Заранее резервируем память под все интервалы.
    histogram.reserve(bins);

    // Ширина каждого интервала одинакова.
    // Вся рабочая область [0, 1] делится на bins равных частей.
    // Поэтому ширина одного интервала равна
    // (правая граница - левая граница) / число интервалов.
    const double width =
        (TVariantDistribution::SupportMax() - TVariantDistribution::SupportMin()) /
        static_cast<double>(bins);

    // Создаем все интервалы вида [left, right).
    // На этом этапе:
    // - left и right задают геометрию столбца;
    // - count пока равен 0, потому что выборку мы еще не разложили;
    // - density тоже пока 0.0, она будет вычислена позже.
    for (std::size_t index = 0; index < bins; ++index)
    {
        const double left = TVariantDistribution::SupportMin() + static_cast<double>(index) * width;
        histogram.push_back({left, left + width, 0, 0.0});
    }

    // Раскладываем каждый элемент выборки по соответствующему интервалу.
    // Здесь реализуется самый прямой смысл гистограммы:
    // нужно понять, в какой "корзине" лежит каждое значение выборки.
    for (double value : samples)
    {
        // shifted показывает, насколько текущее значение сдвинуто
        // относительно левой границы носителя.
        const double shifted = value - TVariantDistribution::SupportMin();

        // Деление shifted / width показывает номер интервала,
        // в который должно попасть значение.
        //
        // Например:
        // - если width = 0.1 и value = 0.34,
        //   то shifted / width = 3.4;
        // - после приведения к целому получаем индекс 3,
        //   то есть четвертый интервал.
        std::size_t binIndex = static_cast<std::size_t>(shifted / width);
        // Для правой границы x = 1 дополнительно защищаемся,
        // чтобы индекс не вышел за пределы массива.
        //
        // Это нужно потому, что для value = 1
        // отношение shifted / width может дать ровно bins,
        // а допустимые индексы вектора - от 0 до bins - 1.
        if (binIndex >= bins)
        {
            binIndex = bins - 1;
        }
        // Увеличиваем число элементов, попавших в соответствующий интервал.
        histogram[binIndex].count += 1;
    }

    // Переводим количество попаданий в оценку плотности:
    // count / (n * width).
    //
    // Почему именно так:
    // - count / n дает относительную частоту попадания в интервал;
    // - деление еще и на width переводит эту частоту
    //   в оценку именно плотности, а не просто вероятности интервала.
    //
    // Благодаря этому площадь столбца
    // density * width
    // соответствует вероятности попасть в данный интервал.
    for (HistogramBin& bin : histogram)
    {
        bin.density =
            static_cast<double>(bin.count) /
            (static_cast<double>(samples.size()) * width);
    }

    // Возвращаем полностью сформированную гистограмму.
    return histogram;
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ KolmogorovDistributionCdf - ФУНКЦИЯ РАСПРЕДЕЛЕНИЯ СТАТИСТИКИ K
// ----------------------------------------------------------------------------
// В критерии Колмогорова используется не только сама статистика K_n,
// но и ее теоретическое распределение.
// Данная функция численно вычисляет значение функции распределения Колмогорова
// по знакопеременному ряду.
//
// Принимает:
// - value: значение аргумента, в котором нужно вычислить функцию распределения.
//
// Возвращает:
// - значение функции распределения Колмогорова в точке value.
// ----------------------------------------------------------------------------
double KolmogorovDistributionCdf(double value)
{
    // Для неположительных значений аргумента функция распределения равна нулю.
    // Это естественно для распределения статистики Колмогорова:
    // вероятность получить значение меньше либо равное числу <= 0 равна 0.
    if (!(value > 0.0))
    {
        return 0.0;
    }

    // Сюда суммируем члены ряда.
    // Функция распределения Колмогорова не записывается здесь через простую
    // элементарную формулу, поэтому используем ее представление рядом.
    double seriesSum = 0.0;
    for (int index = 1; index <= 100; ++index)
    {
        // В ряде знак чередуется: +, -, +, -, ...
        // Именно поэтому в формуле появляется условный выбор знака
        // в зависимости от четности номера члена.
        const double sign = (index % 2 == 1) ? 1.0 : -1.0;
        // exponent - показатель степени для экспоненты очередного члена ряда.
        // Чем больше index или value, тем быстрее экспонента стремится к нулю.
        const double exponent = -2.0 * static_cast<double>(index * index) * value * value;
        // term - очередной член ряда с уже учтенным знаком.
        const double term = sign * std::exp(exponent);
        // Накапливаем сумму членов ряда.
        seriesSum += term;

        // Если очередной член уже практически ничего не меняет,
        // прекращаем суммирование.
        // Это ускоряет вычисление:
        // нет смысла суммировать члены, которые уже слишком малы
        // и не влияют заметно на итоговый результат.
        if (std::abs(term) < 1e-12)
        {
            break;
        }
    }

    // Формула функции распределения Колмогорова имеет вид
    // 1 - 2 * seriesSum.
    //
    // Ограничиваем результат диапазоном [0, 1],
    // чтобы не получить выход за границы из-за численных погрешностей.
    // std::clamp просто "обрезает" число:
    // - если оно меньше 0, возвращается 0;
    // - если больше 1, возвращается 1;
    // - иначе возвращается само вычисленное значение.
    return std::clamp(1.0 - 2.0 * seriesSum, 0.0, 1.0);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ KolmogorovCriticalValue - КРИТИЧЕСКОЕ ЗНАЧЕНИЕ K_(1-alpha)
// ----------------------------------------------------------------------------
// По теории ЛР нулевая гипотеза отвергается,
// если наблюдаемое значение K_n больше критического K_(1-alpha).
//
// Здесь критическое значение ищется численно:
// 1) задаем целевую вероятность 1 - alpha;
// 2) находим отрезок, на котором лежит ответ;
// 3) уточняем ответ методом деления пополам.
//
// Принимает:
// - alpha: уровень значимости критерия.
//
// Возвращает:
// - критическое значение K_(1-alpha).
// ----------------------------------------------------------------------------
double KolmogorovCriticalValue(double alpha)
{
    // Уровень значимости должен лежать строго между 0 и 1.
    // Если alpha <= 0 или alpha >= 1, критерий Колмогорова
    // в такой форме теряет смысл.
    if (!(alpha > 0.0 && alpha < 1.0))
    {
        throw std::invalid_argument("Уровень значимости alpha должен принадлежать интервалу (0, 1)");
    }

    // Нам нужен квантиль уровня 1 - alpha.
    // Именно с этим значением затем сравнивается наблюдаемая статистика K_n.
    //
    // Например:
    // - если alpha = 0.05,
    // - то нам нужен такой порог, левее которого лежит 95% распределения.
    const double targetProbability = 1.0 - alpha;

    // Начальный поисковый отрезок.
    // left и right задают границы интервала,
    // внутри которого мы будем искать критическое значение.
    double left = 0.0;
    double right = 3.0;

    // Если правой границы еще недостаточно,
    // расширяем отрезок, пока искомый квантиль точно не попадет внутрь.
    //
    // Идея простая:
    // если в точке right функция распределения еще меньше targetProbability,
    // значит критическое значение находится правее.
    // Поэтому постепенно двигаем right дальше.
    while (KolmogorovDistributionCdf(right) < targetProbability)
    {
        right *= 2.0;
    }

    // Бинарный поиск по отрезку [left, right].
    // На каждом шаге:
    // - берем середину отрезка;
    // - смотрим, меньше ли там значение функции распределения,
    //   чем нужная целевая вероятность;
    // - если меньше, значит искомый квантиль лежит правее;
    // - иначе он лежит левее либо совпадает с текущей точкой.
    for (int iteration = 0; iteration < 80; ++iteration)
    {
        // Середина текущего поискового интервала.
        const double middle = 0.5 * (left + right);
        if (KolmogorovDistributionCdf(middle) < targetProbability)
        {
            // Если targetProbability еще не достигнута,
            // сдвигаем левую границу вправо.
            left = middle;
        }
        else
        {
            // Если targetProbability уже достигнута или превышена,
            // сдвигаем правую границу влево.
            right = middle;
        }
    }

    // После серии делений пополам left и right становятся очень близки.
    // В качестве ответа берем середину между ними.
    return 0.5 * (left + right);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ EvaluateKolmogorov - КРИТЕРИЙ КОЛМОГОРОВА
// ----------------------------------------------------------------------------
// Теория ЛР задает:
// D_n = sup |F^_n(x) - F(x)|
// K_n = sqrt(n) * D_n.
//
// Здесь реализуется этот критерий для отсортированной выборки:
// 1) по каждому x_i сравниваются эмпирическая и теоретическая функции
//    распределения;
// 2) находятся верхнее и нижнее максимальные отклонения;
// 3) затем вычисляются D_n, K_n и принимается решение по H0.
//
// Принимает:
// - sortedSamples: выборку, отсортированную по возрастанию;
// - alpha: уровень значимости для критерия Колмогорова.
//
// Возвращает:
// - структуру KolmogorovResult с D_n, K_n, критическим значением
//   и итоговым решением по гипотезе H0.
// ----------------------------------------------------------------------------
KolmogorovResult EvaluateKolmogorov(const std::vector<double>& sortedSamples, double alpha)
{
    // Критерий применим только к непустой выборке.
    // Без наблюдений нельзя построить эмпирическую функцию распределения
    // и, следовательно, нельзя вычислить отклонение от теоретической.
    if (sortedSamples.empty())
    {
        throw std::invalid_argument("Для критерия Колмогорова требуется непустая выборка");
    }

    // Размер выборки далее нужен в вещественном виде.
    // Мы приводим его к double, чтобы удобно выполнять деление
    // при вычислении значений эмпирической функции распределения.
    const double sampleSize = static_cast<double>(sortedSamples.size());

    // dPlus и dMinus соответствуют верхнему и нижнему отклонениям.
    // В критерии Колмогорова важно учитывать обе стороны расхождения:
    // - насколько эмпирическая функция идет выше теоретической;
    // - насколько она идет ниже теоретической.
    double dPlus = 0.0;
    double dMinus = 0.0;

    // Проходим по отсортированной выборке.
    // Сортировка обязательна, потому что эмпирическая функция распределения
    // строится по упорядоченным значениям x_(i).
    for (std::size_t index = 0; index < sortedSamples.size(); ++index)
    {
        // Текущее значение x_(i) из отсортированной выборки.
        const double x = sortedSamples[index];
        // Теоретическая функция распределения в точке x.
        // Это то значение, которое должно было бы быть у "идеального" закона.
        const double theoretical = TVariantDistribution::Distribution(x);
        // Правая граница скачка эмпирической функции распределения.
        // После прохождения точки x_(i) эмпирическая функция принимает значение
        // (i + 1) / n.
        const double empiricalUpper =
            static_cast<double>(index + 1) / sampleSize;
        // Левая граница скачка эмпирической функции распределения.
        // Непосредственно перед скачком в точке x_(i) эмпирическая функция
        // еще равна i / n.
        const double empiricalLower =
            static_cast<double>(index) / sampleSize;

        // Накапливаем максимальные отклонения сверху и снизу.
        //
        // dPlus отвечает за отклонение вида
        // F^_n(x) - F(x),
        // то есть когда эмпирическая функция выше теоретической.
        //
        // dMinus отвечает за отклонение вида
        // F(x) - F^_n(x),
        // то есть когда теоретическая функция выше эмпирической.
        dPlus = std::max(dPlus, empiricalUpper - theoretical);
        dMinus = std::max(dMinus, theoretical - empiricalLower);
    }

    // Формируем итог критерия Колмогорова.
    // В эту структуру складываем все основные результаты проверки гипотезы.
    KolmogorovResult result;
    // D_n - максимальное из двух односторонних отклонений.
    // Это и есть величина
    // sup |F^_n(x) - F(x)|.
    result.dn = std::max(dPlus, dMinus);
    // K_n = sqrt(n) * D_n.
    // Умножение на sqrt(n) переводит статистику к стандартной форме критерия.
    result.kn = std::sqrt(sampleSize) * result.dn;
    // Находим критическое значение для заданного alpha.
    // Это порог, с которым сравнивается наблюдаемое значение K_n.
    result.criticalValue = KolmogorovCriticalValue(alpha);
    // Если K_n больше критического значения, нулевая гипотеза отвергается.
    // Иначе считается, что по выборке нет оснований отвергнуть H0.
    result.rejectNullHypothesis = result.kn > result.criticalValue;
    return result;
}
