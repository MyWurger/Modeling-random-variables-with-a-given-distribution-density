// ----------------------------------------------------------------------------
// МЕТОД TVariantDistribution::Density - ПЛОТНОСТЬ РАСПРЕДЕЛЕНИЯ
// ----------------------------------------------------------------------------
// Для нашего варианта:
// f(x) = 2x при x из [0, 1]
// и 0 вне этого отрезка.
//
// Принимает:
// - x: точку, в которой нужно вычислить плотность.
//
// Возвращает:
// - значение плотности f(x).
// ----------------------------------------------------------------------------
double TVariantDistribution::Density(double x)
{
    // Если точка лежит вне носителя распределения,
    // плотность равна нулю.
    //
    // Это соответствует определению нашего варианта:
    // случайная величина принимает значения только на отрезке [0, 1].
    if (x < SupportMin() || x > SupportMax())
    {
        return 0.0;
    }

    // На самом отрезке [0, 1] плотность линейно возрастает
    // по формуле f(x) = 2x.
    return 2.0 * x;
}

// ----------------------------------------------------------------------------
// МЕТОД TVariantDistribution::Distribution - ФУНКЦИЯ РАСПРЕДЕЛЕНИЯ
// ----------------------------------------------------------------------------
// Для нашего варианта:
// F(x) = 0 при x < 0,
// F(x) = x^2 при x из [0, 1],
// F(x) = 1 при x > 1.
//
// Принимает:
// - x: точку, в которой нужно вычислить функцию распределения.
//
// Возвращает:
// - значение F(x).
// ----------------------------------------------------------------------------
double TVariantDistribution::Distribution(double x)
{
    // Если точка находится левее носителя
    // или совпадает с его левой границей,
    // накопленная вероятность еще равна нулю.
    if (x <= SupportMin())
    {
        return 0.0;
    }

    // Если точка лежит правее носителя
    // или совпадает с его правой границей,
    // вся вероятность уже накоплена, поэтому F(x) = 1.
    if (x >= SupportMax())
    {
        return 1.0;
    }

    // На рабочем интервале [0, 1] функция распределения
    // получается интегрированием плотности:
    // F(x) = integral from 0 to x of 2t dt = x^2.
    return x * x;
}

// ----------------------------------------------------------------------------
// МЕТОД TVariantDistribution::Quantile - ОБРАТНАЯ ФУНКЦИЯ РАСПРЕДЕЛЕНИЯ
// ----------------------------------------------------------------------------
// Так как на рабочем интервале F(x) = x^2,
// то обратная функция имеет вид:
// x = sqrt(p).
//
// В данном проекте метод обратной функции реализован численно,
// как требует теория ЛР, но аналитическая квантиль все равно полезна:
// - как часть описания варианта;
// - как эталонная формула распределения.
//
// Принимает:
// - probability: значение вероятности p из диапазона [0, 1].
//
// Возвращает:
// - такое значение x, для которого F(x) = probability.
// ----------------------------------------------------------------------------
double TVariantDistribution::Quantile(double probability)
{
    // Если вероятность не больше нуля,
    // возвращаем самую левую точку носителя.
    // Левее нее функция распределения уже не может дать осмысленный результат.
    if (probability <= 0.0)
    {
        return SupportMin();
    }

    // Если вероятность не меньше единицы,
    // возвращаем правую границу носителя.
    // Это соответствует полной накопленной вероятности.
    if (probability >= 1.0)
    {
        return SupportMax();
    }

    // На интервале [0, 1] функция распределения имеет вид F(x) = x^2.
    // Тогда обратная функция получается из уравнения
    // x^2 = probability,
    // откуда x = sqrt(probability).
    return std::sqrt(probability);
}

// ----------------------------------------------------------------------------
// ФУНКЦИЯ RunSampling - ГЛАВНАЯ ТОЧКА ВХОДА В ВЫЧИСЛИТЕЛЬНОЕ ЯДРО
// ----------------------------------------------------------------------------
// Эта функция объединяет весь алгоритм моделирования:
// 1) проверяет входные параметры;
// 2) запускает один из двух методов генерации выборки;
// 3) сортирует выборку;
// 4) строит гистограмму;
// 5) вычисляет статистики;
// 6) применяет критерий Колмогорова;
// 7) возвращает полный результат для GUI.
//
// Принимает:
// - options: все входные параметры моделирования, выбранные в интерфейсе.
//
// Возвращает:
// - структуру SamplingResult со статусом выполнения, выборкой,
//   статистиками, гистограммой и результатом критерия Колмогорова.
// ----------------------------------------------------------------------------
SamplingResult RunSampling(const SamplingOptions& options)
{
    try
    {
        // Проверяем базовую корректность числа элементов выборки.
        // Без этого невозможно сформировать корректный результат моделирования.
        if (options.sampleSize == 0)
        {
            return {false, "Размер выборки должен быть больше нуля"};
        }

        // Проверяем корректность числа интервалов гистограммы.
        // Нулевая гистограмма не имеет смысла, потому что диапазон
        // должен быть разбит хотя бы на один интервал.
        if (options.histogramBins == 0)
        {
            return {false, "Число интервалов гистограммы должно быть больше нуля"};
        }

        // Создаем результирующую структуру,
        // которую затем отдадим обратно в интерфейс.
        SamplingResult result;
        result.success = true;
        result.method = options.method;

        // Выбираем алгоритм генерации в зависимости от выбранного пользователем
        // метода моделирования.
        // Здесь происходит развилка между двумя основными методами ЛР.
        if (options.method == SamplingMethod::InverseFunction)
        {
            // Генерируем выборку методом обратной функции.
            // Внутри этого вызова используется численное накопление вероятности
            // и поиск точки, в которой выполняется условие S >= k.
            result.samples =
                GenerateInverseFunctionSamples(options.sampleSize, options.integrationStep);
            // Для этого метода доля принятия не имеет смысла,
            // поэтому используем специальное значение.
            result.acceptanceRate = -1.0;
            result.message = "Моделирование методом обратной функции завершено";
        }
        else
        {
            // Генерируем выборку методом исключения.
            // Внутри этого вызова происходит генерация точек-кандидатов (y, z)
            // и отбор только тех значений y, которые прошли условие принятия.
            auto generated = GenerateRejectionSamples(options.sampleSize);
            result.samples = std::move(generated.first);
            // Для метода исключения дополнительно сохраняем
            // долю принятых точек.
            result.acceptanceRate = generated.second;
            result.message = "Моделирование методом исключения завершено";
        }

        // Строим отсортированную версию выборки:
        // она нужна для медианы и критерия Колмогорова.
        // Исходная выборка сохраняется как есть,
        // а sortedSamples используется для статистической обработки.
        result.sortedSamples = result.samples;
        std::sort(result.sortedSamples.begin(), result.sortedSamples.end());

        // Строим гистограмму распределения по выборке.
        // Это выполняет одно из прямых требований лабораторной работы.
        result.histogram = BuildHistogram(result.samples, options.histogramBins);
        // Вычисляем выборочные статистики.
        // Здесь получаем среднее, дисперсию, минимум, максимум, медиану и т.д.
        result.empiricalStatistics = ComputeStatistics(result.samples, result.sortedSamples);
        // Проверяем согласие выборки с теоретическим законом.
        // На этом шаге вычисляются D_n, K_n, критическое значение
        // и решение по нулевой гипотезе H0.
        result.kolmogorov = EvaluateKolmogorov(result.sortedSamples, options.alpha);

        // Возвращаем полностью заполненный результат вычислений.
        return result;
    }
    catch (const std::exception& exception)
    {
        // Любую внутреннюю ошибку превращаем в корректный результат с флагом
        // неуспеха, чтобы GUI смог показать понятное сообщение пользователю.
        return {false, exception.what()};
    }
}
