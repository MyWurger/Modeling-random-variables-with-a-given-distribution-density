// Защита от повторного включения заголовочного файла.
#pragma once
// std::size_t нужен для хранения размеров выборки и числа интервалов.
#include <cstddef>
// std::string нужен для текстовых сообщений о результате расчета.
#include <string>
// std::vector нужен для хранения выборки и массива интервалов гистограммы.
#include <vector>

// ----------------------------------------------------------------------------
// ПЕРЕЧИСЛЕНИЕ SamplingMethod
// ----------------------------------------------------------------------------
// Назначение:
// Описать доступные методы генерации выборки.
//
// В данной лабораторной работе поддерживаются два метода:
// - метод обратной функции;
// - метод исключения.
// ----------------------------------------------------------------------------
enum class SamplingMethod
{
    // Метод обратной функции.
    InverseFunction = 0,
    // Метод исключения.
    Rejection = 1,
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingOptions
// ----------------------------------------------------------------------------
// Назначение:
// Хранить полный набор входных параметров моделирования.
//
// Эта структура формируется в GUI на основе значений полей ввода, а затем
// передается в вычислительное ядро.
// ----------------------------------------------------------------------------
struct SamplingOptions
{
    // Выбранный метод генерации выборки.
    SamplingMethod method = SamplingMethod::InverseFunction;
    // Размер генерируемой выборки.
    std::size_t sampleSize = 10000;
    // Число интервалов гистограммы.
    std::size_t histogramBins = 25;
    // Шаг интегрирования Δy для метода обратной функции.
    double integrationStep = 0.001;
    // Уровень значимости α для критерия Колмогорова.
    double alpha = 0.05;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА HistogramBin
// ----------------------------------------------------------------------------
// Назначение:
// Хранить один интервал гистограммы и его численные характеристики.
//
// Один такой объект соответствует одному столбцу гистограммы:
// - левая граница;
// - правая граница;
// - число попавших элементов;
// - оценка плотности на этом интервале.
// ----------------------------------------------------------------------------
struct HistogramBin
{
    // Левая граница интервала.
    double left = 0.0;
    // Правая граница интервала.
    double right = 0.0;
    // Число элементов выборки, попавших в интервал.
    std::size_t count = 0;
    // Оценка плотности на данном интервале.
    double density = 0.0;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingStatistics
// ----------------------------------------------------------------------------
// Назначение:
// Хранить основные выборочные статистические характеристики.
//
// Эти значения затем выводятся пользователю в итоговой сводке результатов.
// ----------------------------------------------------------------------------
struct SamplingStatistics
{
    // Выборочное среднее.
    double mean = 0.0;
    // Выборочная дисперсия.
    double variance = 0.0;
    // Выборочное среднеквадратическое отклонение.
    double standardDeviation = 0.0;
    // Минимум выборки.
    double minimum = 0.0;
    // Максимум выборки.
    double maximum = 0.0;
    // Выборочная медиана.
    double median = 0.0;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА KolmogorovResult
// ----------------------------------------------------------------------------
// Назначение:
// Хранить результат проверки гипотезы по критерию Колмогорова.
//
// Содержит:
// - статистику Dn;
// - статистику Kn;
// - критическое значение;
// - итоговое решение по нулевой гипотезе H0.
// ----------------------------------------------------------------------------
struct KolmogorovResult
{
    // Максимальное расстояние между эмпирической и теоретической функциями распределения.
    double dn = 0.0;
    // Статистика Колмогорова Kn = sqrt(n) * Dn.
    double kn = 0.0;
    // Критическое значение для выбранного уровня значимости.
    double criticalValue = 0.0;
    // Признак того, что нулевая гипотеза отвергается.
    bool rejectNullHypothesis = false;
};

// ----------------------------------------------------------------------------
// СТРУКТУРА SamplingResult
// ----------------------------------------------------------------------------
// Назначение:
// Хранить полный результат моделирования целиком.
//
// Содержит:
// - флаг успеха;
// - текстовое сообщение;
// - исходную выборку;
// - отсортированную выборку;
// - гистограмму;
// - выборочные статистики;
// - результат критерия Колмогорова;
// - долю принятия для метода исключения.
// ----------------------------------------------------------------------------
struct SamplingResult
{
    // Признак успешного завершения расчета.
    bool success = false;
    // Текстовое сообщение о результате моделирования.
    std::string message;
    // Метод, которым была получена выборка.
    SamplingMethod method = SamplingMethod::InverseFunction;
    // Исходная сгенерированная выборка.
    std::vector<double> samples;
    // Та же выборка, но отсортированная по возрастанию.
    std::vector<double> sortedSamples;
    // Набор интервалов построенной гистограммы.
    std::vector<HistogramBin> histogram;
    // Основные выборочные статистики.
    SamplingStatistics empiricalStatistics;
    // Результат критерия Колмогорова.
    KolmogorovResult kolmogorov;

    // Доля принятия точек в методе исключения.
    // Для метода обратной функции это поле не используется и остается отрицательным.
    double acceptanceRate = -1.0;
};
